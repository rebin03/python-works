# Data types in python

# primitive data type
    # 1. int
    # 2. float
    # 3. complex
    # 4. bool
    # 5. str
    # 6. bytes

# type(object) return data type of specified object

# Examples:
x = 10  # int
print(type(x))

y = 10.5  # float
print(type(y))

z = 10 + 5j  # complex
print(type(z))

is_admin = True  # bool
print(type(is_admin))

name = "John"  # str
print(type(name))

binary_data = b"Hello"  # bytes
print(type(binary_data))


# multi-line text
multi_line_text = """This is a 
multi-line
text
"""
print(type(multi_line_text))