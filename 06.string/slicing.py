#Slicing in python
#Slicing is a way to extract elements from an array
#Slicing is done using the colon operator

# string_obj[start:end]
# string_obj[start:]
# string_obj[:end]


text = "Hellopython"

sliced_str1 = text[0:5]
sliced_str2 = text[5:]
sliced_str3 = text[:5]

print(sliced_str1)
print(sliced_str2)
print(sliced_str3)